public class PR1_2 {
    public void startOz(String s) {
        if (s.charAt(0) == 'o') {
            if (s.charAt(1) == 'z') {
                System.out.println("oz");
            } else {
                System.out.println("o");
            }
        }
        else{
            if(s.charAt(1)=='z'){
                System.out.println("z");
            }
        }
    }
}
